
public class EE {

	public static void main(String[] args) {

		int num1 = 10;

		int num2 = 20;

		int num3 = num1 + num2;

		System.out.println(num1+"+"+num2+"+"+num3);//+는 문자열로 취급
		System.out.println(num3);
		System.out.println(10+"+"+20+"="+10+20);//문자로 인식
		System.out.println(10+"+"+20+"="+(10+20));//괄호 치면 숫자로 인식

	}

}
